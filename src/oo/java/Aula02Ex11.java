package oo.java;
public class Aula02Ex11 {
    public static void main(String[] args) {
        //Operadores Lógicos
        int a = 20, b = 30, c = 1, d = 20;
        System.out.println("\n---------- Operadores Lógicos ----------\n".toUpperCase());
        System.out.println(b < a || c ==1); // OR -> OU
        System.out.println(a == d && d!= a); // AND -> E
        System.out.println(!(c > b)); // NOT -> NÃO
        System.out.println("\n---------- Fim ----------".toUpperCase());
    }
}