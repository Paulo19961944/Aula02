package oo.java;
public class Aula2Ex10 {
    public static void main(String[] args) {
        // Operadores Relacionais
        int a = 20, b = 30, c = 1, d = 20;
        System.out.println("\n---------- Operadores Relacionais ----------\n".toUpperCase());
        System.out.println(b < a); // b é menor que a
        System.out.println(a == d); // a é igual a d
        System.out.println(c > b); // c é maior que b
        System.out.println(a != d); // a é diferente de d
        System.out.println(a < d); // a é menor que d
        System.out.println(a <=d); // a é menor ou igual a d
        System.out.println(c >=b); // c é menor ou igual a b
        System.out.println("\n---------- Fim ----------".toUpperCase());
    }
}